package entidad;

public class Materia {

	private int IdMateria;
	private String Nombre;
	
	

	public int getIdMateria() {
		return IdMateria;
	}

	public void setIdMateria(int idMateria) {
		IdMateria = idMateria;
	}
	
	public String getNombre() {
		return Nombre;
	}
	
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	
}
